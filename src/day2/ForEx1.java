package day2;

public class ForEx1 {

	public static void main(String[] args) {
		// 반복문 - for문 기본 예제 : hello world 5번 출력
		int i;
		for(i=1;i<=5;i++) {
			System.out.println("hello world");
		}
		System.out.println("----------------------------");
		for(i=0;i<5;i++) {
			System.out.println("ㅜㅡㅜㅡㅜㅜㅜ");
		}
	
		
		System.out.println("----------------------------");
		//for문 기본 예제 ; 1부터 5까지 출력하는 예제
		for (i=1; i<=5;i++) {
			System.out.println(i);
		}
		

		}


		
}
